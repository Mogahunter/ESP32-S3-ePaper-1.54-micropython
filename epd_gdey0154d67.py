class TextRenderer:
    """文字渲染器 - 已修复方向问题"""

    def __init__(self, epd):
        self.epd = epd

    def draw_char(self, char, x, y, color=0, bg_color=1):
        """绘制单个字符 (8x8字体) - Y坐标已修正"""
        if char not in FONT_8X8:
            return 8

        font_data = FONT_8X8[char]

        # 字体数据从上到下存储，但屏幕Y=0在底部
        # 使用翻转后的Y坐标
        for row in range(8):
            byte = font_data[row]
            for col in range(8):
                bit = (byte >> (7 - col)) & 1
                pixel_color = color if bit else bg_color
                # Y坐标翻转：row 0 对应屏幕顶部
                self.epd.set_pixel(x + col, y + (7 - row), pixel_color)

        return 8

    def draw_text(self, text, x, y, color=0, bg_color=1, spacing=1):
        """绘制文本字符串"""
        current_x = x
        for char in text:
            width = self.draw_char(char, current_x, y, color, bg_color)
            current_x += width + spacing
        return current_x - x - spacing

    def get_text_width(self, text, spacing=1):
        """计算文本宽度"""
        return len(text) * 8 + (len(text) - 1) * spacing

    def draw_text_centered(self, text, y, color=0, bg_color=1, spacing=1):
        """在屏幕水平居中绘制文本"""
        width = self.get_text_width(text, spacing)
        x = (EPD_WIDTH - width) // 2
        self.draw_text(text, x, y, color, bg_color, spacing)


def create_epd(mode=EPD_GDEY0154D67.MODE_FULL):
    """创建并初始化EPD屏幕"""
    epd = EPD_GDEY0154D67()
    epd.power_on()
    epd.init(mode)
    renderer = TextRenderer(epd)
    return epd, renderer