"""
GDEY0154D67 1.54寸电子墨水屏驱动程序 (SSD1681控制器)
适用于ESP32-S3平台，MicroPython环境

"""

from machine import Pin, SPI
import time

# ========== 引脚定义 ==========
EPD3V3_EN = 6     # 电源使能，输出低电平导通MOS管给屏幕供电
EPD_BUSY = 8      # 忙信号输入
EPD_RST = 9       # 复位输出
EPD_DC = 10       # 数据/命令选择
EPD_CS = 11       # 片选
EPD_SCLK = 12     # SPI时钟
EPD_SDI = 13      # SPI数据输入(MOSI)

# ========== 屏幕参数 ==========
EPD_WIDTH = 200   # 屏幕宽度
EPD_HEIGHT = 200  # 屏幕高度
EPD_BUFFER_SIZE = int(EPD_WIDTH * EPD_HEIGHT / 8)  # 5000字节


class EPD_GDEY0154D67:
    """GDEY0154D67电子墨水屏驱动类"""

    MODE_FULL = 0
    MODE_FAST = 1
    MODE_PARTIAL = 2

    def __init__(self):
        self.epd3v3_en = Pin(EPD3V3_EN, Pin.OUT, value=1)
        self.busy = Pin(EPD_BUSY, Pin.IN)
        self.rst = Pin(EPD_RST, Pin.OUT)
        self.dc = Pin(EPD_DC, Pin.OUT)
        self.cs = Pin(EPD_CS, Pin.OUT, value=1)
        self.spi = SPI(2, baudrate=10000000, polarity=0, phase=0,
                       sck=Pin(EPD_SCLK), mosi=Pin(EPD_SDI), miso=None)
        self.buffer = bytearray(EPD_BUFFER_SIZE)
        self.current_mode = self.MODE_FULL
        print("[EPD] 驱动初始化完成")

    def power_on(self):
        print("[EPD] 开启屏幕电源...")
        self.epd3v3_en.value(0)
        time.sleep_ms(100)

    def power_off(self):
        print("[EPD] 关闭屏幕电源...")
        self.epd3v3_en.value(1)

    def write_command(self, cmd):
        self.dc.value(0)
        self.cs.value(0)
        self.spi.write(bytes([cmd]))
        self.cs.value(1)

    def write_data(self, data):
        self.dc.value(1)
        self.cs.value(0)
        if isinstance(data, int):
            self.spi.write(bytes([data]))
        else:
            self.spi.write(data)
        self.cs.value(1)

    def wait_busy(self, timeout_ms=5000):
        timeout = 0
        while self.busy.value() == 1:
            time.sleep_ms(10)
            timeout += 10
            if timeout > timeout_ms:
                print("[EPD] 警告：等待超时")
                return False
        return True

    def reset(self):
        print("[EPD] 复位屏幕...")
        self.rst.value(0)
        time.sleep_ms(10)
        self.rst.value(1)
        time.sleep_ms(10)
        self.wait_busy()

    def init(self, mode=MODE_FULL):
        if mode == self.MODE_FULL:
            self._init_full()
        elif mode == self.MODE_FAST:
            self._init_fast()
        else:
            self._init_full()
        self.current_mode = mode

    def _init_full(self):
        print("[EPD] 全刷初始化...")
        self.reset()
        self.write_command(0x12)
        self.wait_busy()

        self.write_command(0x01)
        self.write_data(0xC7)
        self.write_data(0x00)
        self.write_data(0x00)

        self.write_command(0x11)
        self.write_data(0x01)

        self.write_command(0x44)
        self.write_data(0x00)
        self.write_data(0x18)

        self.write_command(0x45)
        self.write_data(0xC7)
        self.write_data(0x00)
        self.write_data(0x00)
        self.write_data(0x00)

        self.write_command(0x3C)
        self.write_data(0x05)

        self.write_command(0x18)
        self.write_data(0x80)

        self.write_command(0x4E)
        self.write_data(0x00)

        self.write_command(0x4F)
        self.write_data(0xC7)
        self.write_data(0x00)
        self.wait_busy()
        print("[EPD] 初始化完成")

    def _init_fast(self):
        print("[EPD] 快速刷新初始化...")
        self.reset()
        self.write_command(0x12)
        self.wait_busy()
        self.write_command(0x18)
        self.write_data(0x80)
        self.write_command(0x22)
        self.write_data(0xB1)
        self.write_command(0x20)
        self.wait_busy()
        self.write_command(0x1A)
        self.write_data(0x64)
        self.write_data(0x00)
        self.write_command(0x22)
        self.write_data(0x91)
        self.write_command(0x20)
        self.wait_busy()
        print("[EPD] 快速初始化完成")

    def _update_full(self):
        self.write_command(0x22)
        self.write_data(0xF7)
        self.write_command(0x20)
        self.wait_busy()

    def _update_fast(self):
        self.write_command(0x22)
        self.write_data(0xC7)
        self.write_command(0x20)
        self.wait_busy()

    def clear(self, color=0xFF, update=True):
        self.buffer = bytearray([color] * EPD_BUFFER_SIZE)
        if update:
            self._write_buffer_to_screen()
            if self.current_mode == self.MODE_FAST:
                self._update_fast()
            else:
                self._update_full()

    def _write_buffer_to_screen(self):
        self.write_command(0x24)
        for i in range(0, EPD_BUFFER_SIZE, 256):
            chunk = self.buffer[i:i+256]
            self.write_data(chunk)

    def display(self, mode=None):
        self._write_buffer_to_screen()
        if mode is None:
            mode = self.current_mode
        if mode == self.MODE_FAST:
            self._update_fast()
        else:
            self._update_full()

    def set_pixel(self, x, y, color):
        """设置像素点 - 坐标系已修正"""
        if x < 0 or x >= EPD_WIDTH or y < 0 or y >= EPD_HEIGHT:
            return
        # Y坐标翻转：屏幕物理Y=0在底部，逻辑Y=0在顶部
        y_flipped = EPD_HEIGHT - 1 - y
        byte_idx = y_flipped * 25 + x // 8
        bit_idx = 7 - (x % 8)
        if color == 0:
            self.buffer[byte_idx] &= ~(1 << bit_idx)
        else:
            self.buffer[byte_idx] |= (1 << bit_idx)

    def draw_line(self, x0, y0, x1, y1, color=0):
        dx = abs(x1 - x0)
        dy = abs(y1 - y0)
        sx = 1 if x0 < x1 else -1
        sy = 1 if y0 < y1 else -1
        err = dx - dy
        while True:
            self.set_pixel(x0, y0, color)
            if x0 == x1 and y0 == y1:
                break
            e2 = 2 * err
            if e2 > -dy:
                err -= dy
                x0 += sx
            if e2 < dx:
                err += dx
                y0 += sy

    def draw_rect(self, x, y, w, h, color=0, fill=False):
        if fill:
            for i in range(y, y + h):
                for j in range(x, x + w):
                    self.set_pixel(j, i, color)
        else:
            for i in range(x, x + w):
                self.set_pixel(i, y, color)
                self.set_pixel(i, y + h - 1, color)
            for i in range(y, y + h):
                self.set_pixel(x, i, color)
                self.set_pixel(x + w - 1, i, color)

    def fill(self, color=0xFF):
        for i in range(EPD_BUFFER_SIZE):
            self.buffer[i] = color

    def deep_sleep(self):
        print("[EPD] 进入深度睡眠...")
        self.write_command(0x10)
        self.write_data(0x01)
        time.sleep_ms(100)

    def wake_up(self):
        print("[EPD] 唤醒屏幕...")
        self.init(self.current_mode)


